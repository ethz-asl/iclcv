#include <ICLUtils/DynMatrixUtils.h>
#include <ICLUtils/Random.h>

using namespace icl;

int main(){
  typedef double real;
  
  DynMatrix<real> a(4,2);
  std::fill(a.begin(),a.end(),URand(0,1));

  Time t=Time::now();
  DynMatrix<real> U,s,Vt;
  a.svd(U,s,Vt);

  SHOW(U);
  SHOW(s);
  SHOW(Vt);
  SHOW(t.age().toMilliSecondsDouble());
}
